/*
 * @brief Write one byte to SSD1306
 * @param data Data/Command byte
 * @param cmd 0: cmd, 1: data
 * @retval None
 */
static void _oled_write_byte(uint8_t data, uint8_t cmd)
{
        uint8_t i;

        if (cmd) {
                OLED_DC_SET();
        } else {
                OLED_DC_CLR();
        }

        //OLED_CS_CLR();
        for (i = 0; i < 8; i++) {
                OLED_SCLK_CLR();

                if (data & 0x80) {
                        OLED_SDIN_SET();
                } else {
                        OLED_SDIN_CLR();
                }

                OLED_SCLK_SET();

                data <<= 1;
        }
        //OLED_CS_SET();

        OLED_DC_SET();
}

static void _oled_write_cmd(uint8_t cmd)
{
	_oled_write_byte(cmd, OLED_CMD);
}

static void _oled_write_data(uint8_t data)
{
	_oled_write_byte(data, OLED_DATA);
}

static void _oled_clear(void)
{
        uint8_t i, n;
        for (i = 0; i < 8; i++) {
                _oled_write_cmd(0xb0 + i);  // set page start address
                _oled_write_cmd(0x00);  // set low column address
                _oled_write_cmd(0x10);  // set high column address

                for (n = 0; n < 128; n++) {
                        _oled_write_data(0x0);
                }
        }
}

void _oled_set_pos(uint8_t x, uint8_t y)
{
        _oled_write_cmd(0xb0 + y);
        _oled_write_cmd(((x & 0xF0) >> 4) | 0x10);
        _oled_write_cmd((x & 0x0F) | 0x01);
}

void _oled_display_on(void)
{
        _oled_write_cmd(0x8D);  // set dcdc command
        _oled_write_cmd(0x14);  // dcdc on
        _oled_write_cmd(0xAF);  // display on
}

void _oled_display_off(void)
{
        _oled_write_cmd(0x8D);  // set dcdc command
        _oled_write_cmd(0x10);  // dcdc off
        _oled_write_cmd(0xAE);  // display off
}

void _oled_show_char(uint8_t x, uint8_t y, uint8_t chr)
{
        unsigned char c = 0, i = 0;
        c = chr - ' ';

        if (x > OLED_MAX_COL - 1) {
                x = 0;
                y = y + 2;
        }

        if (OLED_SIZE == 16) {
                _oled_set_pos(x, y);

                for (i = 0; i < 8; i++) {
                        _oled_write_data(F8X16[c][i]);
                }

                _oled_set_pos(x, y + 1);

                for (i = 0; i< 8; i++) {
                        _oled_write_data(F8X16[c][i + 8]);
                }
        } else {
                _oled_set_pos(x, y + 1);

                for (i = 0; i < 6; i++) {
                        _oled_write_data(F6x8[c][i]);
                }
        }
}

void oled_show_string(uint8_t x, uint8_t y, uint8_t *chr)
{
        unsigned char j = 0;
        while (chr[j] != '\0') {
                _oled_show_char(x, y, chr[j]);
                x += 8;

                if (x > 120) {
                        x = 0;
                        y += 2;
                }

                j++;
        }
}

static void _oled_spi_init(void)
{
	GPIO_InitTypeDef gpio_init;

	CONFIG_OLED_SCLK_CLK_FUN(CONFIG_OLED_SCLK_CLK, ENABLE);
	CONFIG_OLED_SDIN_CLK_FUN(CONFIG_OLED_SDIN_CLK, ENABLE);
	CONFIG_OLED_RST_CLK_FUN(CONFIG_OLED_RST_CLK, ENABLE);
	CONFIG_OLED_DC_CLK_FUN(CONFIG_OLED_DC_CLK, ENABLE);

	gpio_init.GPIO_Pin = CONFIG_OLED_SCLK_PIN;
	gpio_init.GPIO_Mode = GPIO_Mode_Out_PP;
	gpio_init.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(CONFIG_OLED_SCLK_PORT, &gpio_init);

	gpio_init.GPIO_Pin = CONFIG_OLED_SDIN_PIN;
	GPIO_Init(CONFIG_OLED_SDIN_PORT, &gpio_init);

	gpio_init.GPIO_Pin = CONFIG_OLED_RST_PIN;
	GPIO_Init(CONFIG_OLED_RST_PORT, &gpio_init);

	gpio_init.GPIO_Pin = CONFIG_OLED_DC_PIN;
	GPIO_Init(CONFIG_OLED_DC_PORT, &gpio_init);
}

void bsp_oled_init(void)
{
	_oled_spi_init();

	OLED_RST_SET();
	mdelay(100);
	OLED_RST_CLR();
	mdelay(200);
	OLED_RST_SET();

	_oled_write_cmd(0xAE);  // turn off oled panel
	_oled_write_cmd(0x00);  // set low column address
	_oled_write_cmd(0x10);  // set high column address
	_oled_write_cmd(0x40);  // set start line address, Set mapping RAM display start line (0x00 ~ 0x3F)
	_oled_write_cmd(0x81);  // set contrast control register
	_oled_write_cmd(0xCF);  // set seg output current brightness
	_oled_write_cmd(0xA1);  // set seg/column mapping
	_oled_write_cmd(0xC8);  // set COM/Row Scan ddirection
	_oled_write_cmd(0xA6);  // set normal display
	_oled_write_cmd(0xA8);  // set multiplex ratio (1 to 64)
	_oled_write_cmd(0x3F);  // 1/64 duty
	_oled_write_cmd(0xD3);  // set display offset, Shift mapping RAM counter (0x00 ~ 0x3F)
	_oled_write_cmd(0x00);  // no offset
	_oled_write_cmd(0xD5);  // set display clock divide ratio/oscillator freqency
	_oled_write_cmd(0x80);  // set divide ratio, set clock as 100 frames/sec
	_oled_write_cmd(0xD9);  // set pre-charge period
	_oled_write_cmd(0xF1);  // set pre-charge as 15 clock & discharge as 1 clock
	_oled_write_cmd(0xDA);  // set com pins hardware configuration
	_oled_write_cmd(0x12);
	_oled_write_cmd(0xDB);  // set Vcomh
	_oled_write_cmd(0x40);  // set Vcom deselect level
	_oled_write_cmd(0x20);  // set page addressing mode (0x00/0x01/0x02);
	_oled_write_cmd(0x02);  // page addressing mode
	_oled_write_cmd(0x8D);  // set charge pump enable/disable
	_oled_write_cmd(0x14);  // set 0x10 disable
	_oled_write_cmd(0xA4);  // disable entire display on (0xa4/0xa5)
	_oled_write_cmd(0xA6);  // disable inverse display on (0xa6/0xa7)
	_oled_write_cmd(0xAF);  // turn on oled panel

	_oled_write_cmd(0xAF);  // disaply ON

        _oled_clear();
        _oled_set_pos(0, 0);
}

